const numeros = [1, 2, 3, 4, 5];

// Adição no final
numeros.push(6); // retorna o novo tamanho do array
console.log(numeros);

// Remoção no final
numeros.pop(); // retorna o elemento removido
console.log(numeros);

// Adição no início
numeros.unshift(0); // retorna o novo tamanho do array
console.log(numeros);

// Remoção no início
numeros.shift(); // retorna o elemento removido
console.log(numeros);
